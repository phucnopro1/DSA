import java.util.Stack;

public class Push {

        public static void main(String[] args) {
            // Create a new Stack
            Stack<String> stack = new Stack<>();

            // Push components to the stack
            stack.push("One");
            stack.push("Eleven");
            stack.push("Twenty one");

            // Print the stack
            System.out.println("Stack: " + stack);
        }
    }

